import fs from "node:fs";
import path from "node:path";

const SHADER_FILES = [
  "filterPass1LiteShader.ts",
  "filterPass2LiteShader.ts",
  "filterPass2BeamLiteShader.ts",
  "filterPass1Pc98LiteShader.ts",
  "filterPass2PhosphorLiteShader.ts",
];

const LEGACY_OUTPUTS = [
  "filterShader.js",
  "filterPass1Shader.js",
  "filterPass2Shader.js",
];

const sourceDir = path.join("src", "retro-player", "retro");
const outputDir = path.join("extension", "shared");

function extractShaderExport(sourcePath, source) {
  const match = source.match(/export const (\w+)\s*=\s*`([\s\S]*?)`;\n?/);
  if (!match) {
    throw new Error(`Could not find shader export in ${sourcePath}`);
  }

  return {
    exportName: match[1],
    shaderSource: match[2],
  };
}

fs.mkdirSync(outputDir, { recursive: true });

for (const fileName of SHADER_FILES) {
  const sourcePath = path.join(sourceDir, fileName);
  const outputPath = path.join(outputDir, fileName.replace(/\.ts$/, ".js"));
  const source = fs.readFileSync(sourcePath, "utf-8");
  const { exportName, shaderSource } = extractShaderExport(sourcePath, source);
  const output = `// Generated by scripts/sync-extension-shader.mjs from ${sourcePath}
export const ${exportName} = \`${shaderSource}\`;
`;

  fs.writeFileSync(outputPath, output);
  console.log(`generated ${outputPath} from ${sourcePath}`);
}

for (const fileName of LEGACY_OUTPUTS) {
  const outputPath = path.join(outputDir, fileName);
  if (!fs.existsSync(outputPath)) {
    continue;
  }

  fs.rmSync(outputPath);
  console.log(`removed legacy ${outputPath}`);
}
