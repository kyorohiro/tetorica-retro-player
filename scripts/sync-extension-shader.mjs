import fs from "node:fs";
import path from "node:path";

const sourcePath = path.join("src", "retro-player", "retro", "filterShader.ts");
const outputDir = path.join("extension", "shared");
const outputPath = path.join(outputDir, "filterShader.js");

const source = fs.readFileSync(sourcePath, "utf-8");
const fragmentMatch = source.match(/export const FILTER_FRAGMENT = `([\s\S]*?)`;\n?/);

if (!fragmentMatch) {
  throw new Error(`Could not find FILTER_FRAGMENT in ${sourcePath}`);
}

const fragmentShader = fragmentMatch[1];
const output = `// Generated by scripts/sync-extension-shader.mjs from ${sourcePath}
export const FILTER_FRAGMENT = \`${fragmentShader}\`;
`;

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputPath, output);
console.log(`generated ${outputPath} from ${sourcePath}`);
